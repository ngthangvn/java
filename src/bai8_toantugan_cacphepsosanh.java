public class bai8_toantugan_cacphepsosanh {
    public static void main(String[] args) {
        //gán cộng
        int x = 8;
        x+=5; // x=x+5
        System.out.println("x = " +x);
        // gán trừ
        int y = 8;
        y-=5; // y=y-5
        System.out.println("y = " +y);
        // gán nhân
        int z = 8;
        z*=5; // z=z*5
        System.out.println("z = " +z);
        // gán chia
        int a = 8;
        a/=5; // a=a/5
        System.out.println("a = " +(double)a);
        // gán chia có dư
        int b = 8;
        b%=5; // b=b%5
        System.out.println("b = " +b);

        int c = 7;
        int d = 9;
        System.out.println(c==d);
        System.out.println(c!=d); //khác
        System.out.println(c>d);
        System.out.println(c<d);
        System.out.println(c<=d);
        System.out.println(c>=d);
    }
}
